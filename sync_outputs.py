"""
Sync the dashboard's figures and tables from outputs_2024/.

What it produces:
  figures/*.pdf      paper-quality vector PDFs, mirrored as-is (the download)
  figures/web/*.webp display images, <= WEB_MAX_WIDTH px wide (what the site shows)
  tables/*.csv       raw CSVs, mirrored as-is (the download)
  data/tables.js     rendered tables embedded as JS (works from file://, no fetch)
  data/figmeta.js    display-image pixel sizes, so <img> gets width/height
                     and the page never shifts while loading

The 300-dpi PNGs are NOT shipped: WebP is ~10x smaller at screen resolution and
the PDF is the high-resolution artifact. Any PNG left in figures/ is purged.

The manifest is cross-validated against the output folder in BOTH directions
and the sync fails loudly on any mismatch, so a pipeline rerun that renames or
drops a figure cannot leave the site silently stale.

Requires Pillow (the only non-stdlib dependency). Run after every
`run_verification.py`:

    python website/sync_outputs.py
"""

import csv
import json
import shutil
import sys
from pathlib import Path

from PIL import Image

HERE = Path(__file__).resolve().parent
SRC = HERE.parent / "outputs_2024"
FIGURES = HERE / "figures"
WEB = FIGURES / "web"
TABLES = HERE / "tables"
DATA_JS = HERE / "data" / "tables.js"
FIGMETA_JS = HERE / "data" / "figmeta.js"

WEB_MAX_WIDTH = 1500   # px; plenty for a ~1100px content column on 2x displays
WEB_QUALITY = 82


def load_manifest():
    """manifest.js is `window.MANIFEST = <pure JSON>;` — extract the JSON."""
    text = (HERE / "manifest.js").read_text()
    start, end = text.index("{"), text.rindex("}")
    return json.loads(text[start:end + 1])


def mirror(patterns, dest):
    """Copy matching source files into dest; delete dest files no longer in src."""
    dest.mkdir(exist_ok=True)
    wanted = {p.name: p for pattern in patterns for p in sorted(SRC.glob(pattern))}
    for stale in dest.iterdir():
        if stale.is_dir():
            continue
        if stale.name not in wanted:
            stale.unlink()
            print(f"  removed stale {dest.name}/{stale.name}")
    for name, src_path in wanted.items():
        shutil.copy2(src_path, dest / name)
    return set(wanted)


def build_webp(manifest):
    """Generate figures/web/<id>.webp for every manifest figure; return
    ({id: [w, h]}, [ids with no source PNG]). Skips figures whose WebP is
    already newer than its source."""
    WEB.mkdir(parents=True, exist_ok=True)
    meta, missing, wanted = {}, [], set()

    for fig in manifest["figures"]:
        fid = fig["id"]
        src = SRC / f"{fid}.png"
        if not src.exists():
            missing.append(fid)
            continue
        out = WEB / f"{fid}.webp"
        wanted.add(out.name)

        if out.exists() and out.stat().st_mtime >= src.stat().st_mtime:
            with Image.open(out) as im:
                meta[fid] = list(im.size)
            continue

        with Image.open(src) as im:
            if im.mode not in ("RGB", "RGBA"):
                im = im.convert("RGB")
            w, h = im.size
            if w > WEB_MAX_WIDTH:
                im = im.resize(
                    (WEB_MAX_WIDTH, round(h * WEB_MAX_WIDTH / w)), Image.LANCZOS)
            im.save(out, "WEBP", quality=WEB_QUALITY, method=6)
            meta[fid] = list(im.size)
        print(f"  webp {out.name} {meta[fid][0]}x{meta[fid][1]} "
              f"({out.stat().st_size // 1024} KB from {src.stat().st_size // 1024} KB)")

    for stale in sorted(WEB.iterdir()):
        if stale.name not in wanted:
            stale.unlink()
            print(f"  removed stale web/{stale.name}")

    FIGMETA_JS.parent.mkdir(exist_ok=True)
    FIGMETA_JS.write_text(
        "// Generated by sync_outputs.py — do not edit by hand.\n"
        "window.FIG_META = " + json.dumps(meta) + ";\n"
    )
    return meta, missing


def build_tables_js(manifest):
    DATA_JS.parent.mkdir(exist_ok=True)
    data = {}
    for spec in manifest["tables"]:
        if not spec.get("render"):
            continue
        with open(TABLES / f"{spec['id']}.csv", newline="") as f:
            rows = list(csv.reader(f))
        data[spec["id"]] = {"columns": rows[0], "rows": rows[1:]}
    DATA_JS.write_text(
        "// Generated by sync_outputs.py — do not edit by hand.\n"
        "window.TABLE_DATA = " + json.dumps(data) + ";\n"
    )
    print(f"  data/tables.js embedded {len(data)} tables")


def main():
    if not SRC.is_dir():
        sys.exit(f"source folder not found: {SRC}")

    manifest = load_manifest()
    print(f"Syncing from {SRC}")
    copied_pdfs = mirror(["*.pdf"], FIGURES)   # also purges stale PNGs/PDFs
    copied_csvs = mirror(["*.csv"], TABLES)
    meta, missing_png = build_webp(manifest)

    problems = []

    # manifest → files: every declared figure/table must exist
    fig_ids = [f["id"] for f in manifest["figures"]]
    for fid in missing_png:
        problems.append(f"manifest figure '{fid}' has no PNG in {SRC.name}/")
    for fig in manifest["figures"]:
        if not fig.get("no_pdf") and f"{fig['id']}.pdf" not in copied_pdfs:
            problems.append(f"manifest figure '{fig['id']}' has no PDF in {SRC.name}/")
    table_ids = [t["id"] for t in manifest["tables"]]
    for tid in table_ids:
        if f"{tid}.csv" not in copied_csvs:
            problems.append(f"manifest table '{tid}' has no CSV in {SRC.name}/")

    # files → manifest: nothing in the output folder may be missing from it
    for path in sorted(SRC.glob("*.png")) + sorted(SRC.glob("*.pdf")):
        if path.stem not in fig_ids:
            problems.append(f"{path.name} exists in {SRC.name}/ but is not in manifest.js")
    for path in sorted(SRC.glob("*.csv")):
        if path.stem not in table_ids:
            problems.append(f"{path.name} exists in {SRC.name}/ but is not in manifest.js")

    if problems:
        print("\nSYNC FAILED — manifest and output folder disagree:", file=sys.stderr)
        for p in problems:
            print(f"  ✗ {p}", file=sys.stderr)
        sys.exit(1)

    build_tables_js(manifest)
    webp_kb = sum(p.stat().st_size for p in WEB.glob("*.webp")) // 1024
    print(f"OK — {len(meta)} WebPs ({webp_kb} KB total), {len(copied_pdfs)} PDFs, "
          f"{len(copied_csvs)} CSVs; {len(fig_ids)} manifest figures, "
          f"{len(table_ids)} manifest tables.")


if __name__ == "__main__":
    main()
