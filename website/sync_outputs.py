"""
Sync the dashboard's figures and tables from mam2024_analysis_outputs/.

Mirrors PNG/PDF files into website/figures/ and CSVs into website/tables/,
regenerates data/tables.js (CSV contents embedded as JS so the site works from
file:// with no fetch/CORS), then cross-validates the manifest against what was
actually copied — in BOTH directions — and fails loudly on any mismatch, so a
pipeline rerun that renames or drops a figure cannot leave the site silently
stale.

Stdlib only. Run after every `run_verification.py`:

    python website/sync_outputs.py
"""

import csv
import json
import shutil
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
SRC = HERE.parent / "mam2024_analysis_outputs"
FIGURES = HERE / "figures"
TABLES = HERE / "tables"
DATA_JS = HERE / "data" / "tables.js"


def load_manifest():
    """manifest.js is `window.MANIFEST = <pure JSON>;` — extract the JSON."""
    text = (HERE / "manifest.js").read_text()
    start, end = text.index("{"), text.rindex("}")
    return json.loads(text[start:end + 1])


def mirror(patterns, dest):
    """Copy matching source files into dest; delete dest files no longer in src."""
    dest.mkdir(exist_ok=True)
    wanted = {p.name: p for pattern in patterns for p in sorted(SRC.glob(pattern))}
    for stale in dest.iterdir():
        if stale.name not in wanted:
            stale.unlink()
            print(f"  removed stale {dest.name}/{stale.name}")
    for name, src_path in wanted.items():
        shutil.copy2(src_path, dest / name)
    return set(wanted)


def build_tables_js(manifest):
    DATA_JS.parent.mkdir(exist_ok=True)
    data = {}
    for spec in manifest["tables"]:
        if not spec.get("render"):
            continue
        with open(TABLES / f"{spec['id']}.csv", newline="") as f:
            rows = list(csv.reader(f))
        data[spec["id"]] = {"columns": rows[0], "rows": rows[1:]}
    DATA_JS.write_text(
        "// Generated by sync_outputs.py — do not edit by hand.\n"
        "window.TABLE_DATA = " + json.dumps(data) + ";\n"
    )
    print(f"  data/tables.js embedded {len(data)} tables")


def main():
    if not SRC.is_dir():
        sys.exit(f"source folder not found: {SRC}")

    manifest = load_manifest()
    print(f"Syncing from {SRC}")
    copied_figs = mirror(["*.png", "*.pdf"], FIGURES)
    copied_csvs = mirror(["*.csv"], TABLES)

    problems = []

    # manifest → files: every declared figure/table must exist
    fig_ids = [f["id"] for f in manifest["figures"]]
    for fig in manifest["figures"]:
        if f"{fig['id']}.png" not in copied_figs:
            problems.append(f"manifest figure '{fig['id']}' has no PNG in {SRC.name}/")
        if not fig.get("no_pdf") and f"{fig['id']}.pdf" not in copied_figs:
            problems.append(f"manifest figure '{fig['id']}' has no PDF in {SRC.name}/")
    table_ids = [t["id"] for t in manifest["tables"]]
    for tid in table_ids:
        if f"{tid}.csv" not in copied_csvs:
            problems.append(f"manifest table '{tid}' has no CSV in {SRC.name}/")

    # files → manifest: nothing copied may be missing from the manifest
    for name in sorted(copied_figs):
        if Path(name).stem not in fig_ids:
            problems.append(f"{name} exists in {SRC.name}/ but is not in manifest.js")
    for name in sorted(copied_csvs):
        if Path(name).stem not in table_ids:
            problems.append(f"{name} exists in {SRC.name}/ but is not in manifest.js")

    if problems:
        print("\nSYNC FAILED — manifest and output folder disagree:", file=sys.stderr)
        for p in problems:
            print(f"  ✗ {p}", file=sys.stderr)
        sys.exit(1)

    build_tables_js(manifest)
    n_png = sum(1 for n in copied_figs if n.endswith(".png"))
    n_pdf = sum(1 for n in copied_figs if n.endswith(".pdf"))
    print(f"OK — {n_png} PNGs, {n_pdf} PDFs, {len(copied_csvs)} CSVs; "
          f"{len(fig_ids)} manifest figures, {len(table_ids)} manifest tables.")


if __name__ == "__main__":
    main()
